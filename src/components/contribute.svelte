	<span class="font-bold"> Contribute </span>

	<p>
		If you are a digital artist wanting to contribute to this collection of connected art pieces,
		follow the steps below accordingly and your art will be hosted here in no time!
	</p>

	<ol>
		<li>Pick an empty box that has a drawing above, beside or below it positions.</li>
		<li>
			Join <a href="https://t.me/TheUnityProjectMuralGroup" target="_blank"
				>@TheUnityProjectMuralGroup</a
			> on Telegram.
		</li>
		<li>
			Check the group for the pinned messages to see if the spot you chose is reserved or not.
		</li>
		<li>If the spot is reserved, pick another spot and recheck.</li>
		<li>
			If the spot is not reserved, quickly text the group in the following format: <code
				>textFormattextFormat</code
			>.
		</li>
		<li>
			When choosing time to finish the drawing, choose from a minimum of 2 hours to a maximum of 2
			days.
		</li>
		<li>If you don't deliver by the time frame, your spot will be given to someone else.</li>
		<li>
			If all goes right, your text will be pinned by the organizers to show that you have
			successfully reserved your spot.
		</li>
		<li>The dimension for your drawing should be any square with a minimum of 500x500 pixels.</li>
		<li>Draw your art piece in a way it connects to the arts beside the spot you chose.</li>
		<li>Also draw in a way that it lets others continue off from your drawing on all sides.</li>
		<li>When you finish drawing, send the uncompressed high-quality file in the group.</li>
		<li>
			The project organizers will then upload your drawing to the website and host it for the world
			to admire!
		</li>
	</ol>

	<p>We'd be honored to have your work!</p>
